package com.chua.utils.tools.common;

/**
 * 日期格式
 *
 * @author CH
 */
public interface DateFormatter {
    /**
     * yyyy-MM-dd HH:mm
     */
    String YYYY_MM_DD$HHMM = "yyyy-MM-dd HH:mm";
    /**
     * yyyy/MM/dd HH:mm
     */
    String YYYY0MM0DD$HHMM = "yyyy/MM/dd HH:mm";
    /**
     * yyyy-MM-dd HH:mm:ss
     */
    String YYYY_MM_DD$HHMMSS = "yyyy-MM-dd HH:mm:ss";
    /**
     * yyyy/MM/dd HH:mm:ss
     */
    String YYYY0MM0DD$HHMMSS = "yyyy/MM/dd HH:mm:ss";
    /**
     * yyyy-MM-dd
     */
    String YYYY_MM_DD = "yyyy-MM-dd";
    /**
     * yyyy/MM/dd
     */
    String YYYY0MM0DD = "yyyy/MM/dd";
    /**
     * yyyy-MM
     */
    String YYYY_MM = "yyyy-MM";
    /**
     * yyyy/MM
     */
    String YYYY0MM = "yyyy/MM";
    /**
     * yyyy
     */
    String YYYY = "yyyy";
    /**
     * MM
     */
    String MM = "MM";
    /**
     * dd
     */
    String DD = "dd";
    /**
     * yyyy-MM-dd HH
     */
    String YYYY_MM_DD$HH = "yyyy-MM-dd HH";
    /**
     * yyyy/MM/dd HH
     */
    String YYYY0M0DD$HH = "yyyy/MM/dd HH";
}
